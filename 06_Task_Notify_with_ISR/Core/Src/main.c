void button_interrupt_handler(void){


	BaseType_t pxHigherPriorityTaskWoken;

	pxHigherPriorityTaskWoken = pdFALSE;
	traceISR_ENTER();

	xTaskNotifyFromISR(next_handler,0,eNoAction,&pxHigherPriorityTaskWoken);

	/* Once the ISR exits, the below macfo makes highe rpirority task which got unblocked to resume on the VPU */
	portYIELD_FROM_ISR(pxHigherPriorityTaskWoken);

	traceISR_EXIT();
	return;
}


